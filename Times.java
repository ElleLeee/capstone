package test;

public class Times {

	private int start;
	private int end;
	
	public Times(int s, int e) {
		this.start = s;
		this.end=e;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}
	
	
}
